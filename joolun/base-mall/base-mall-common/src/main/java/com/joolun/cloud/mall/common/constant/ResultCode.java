package com.joolun.cloud.mall.common.constant;

public enum ResultCode {
    CODE_500(500,"数据未找到"),
    ;
    private int code;
    private String msg;

    ResultCode(int code, String msg) {
        this.code = code;
        this.msg = msg;
    }

    public int getCode() {
        return code;
    }

    public String getMsg() {
        return msg;
    }
}
